    private void BotaoAtualizarFuncionario1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoAtualizarFuncionario1ActionPerformed
        String id = campoTextoExibirID.getText();
        String nome = campoTextoExibirNome.getText();
        String empresa = campoTextoExibirEmpresa.getText();
        String profissao = campoTextoExibirProfissao.getText();
        String salario = campoTextoExibirSalario.getText();
        String salarioSemVirgula = salario.replaceAll(",", "\\.");

        if (nome == null || empresa == null || profissao == null || salario == null) {
            JOptionPane.showMessageDialog(null, "Funcionário inválido. Todos os campos devem ser preenchidos.");
        }

        try {
            Double salarioDb = Double.parseDouble(salarioSemVirgula);
            String salarioStr = salarioDb.toString();
            int i = Integer.parseInt(id);
            if (salarioDb > 0 && i >= 0) {
                validaFuncionario(nome, empresa, profissao);
                FuncionarioRepository funcionarioRepository = new FuncionarioRepository();
                funcionarioRepository.editarFuncionario(i, nome, empresa, profissao, salarioStr);
                JOptionPane.showMessageDialog(null, "Funcionário Atualizado com sucesso.");
                campoTextoExibirID.setText("");
                campoTextoNome.setText("");
                campoTextoExibirEmpresa.setText("");
                campoTextoExibirProfissao.setText("");
                campoTextoExibirSalario.setText("");
                PainelPrincipal.setSelectedComponent(abaConsultarFuncionario);
            } else {
                JOptionPane.showMessageDialog(null, "O salário ou o ID devem ter um valor maior que ZERO.");
            }

        } catch (ValidaFuncionarioException e) {
            JOptionPane.showMessageDialog(null, "Funcionário inválido. O nome, a empresa ou a profissão estão incorretos.");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "O salário ou o ID devem ter um valor válido.");
        }
    }//GEN-LAST:event_BotaoAtualizarFuncionario1ActionPerformed

