    private void botaoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoBuscarActionPerformed
        String busca = campoBuscarFuncionario.getText();
        FuncionarioRepository funcionarioRepository = new FuncionarioRepository();
        ArrayList<Funcionario> lista = funcionarioRepository.buscarFuncionario(busca);
        JList<Funcionario> listaFuncionario = new JList();
        DefaultListModel<Funcionario> model = new DefaultListModel();
        for (Funcionario funcionario : lista) {
            model.addElement(funcionario);
        }
        listaFuncionario.setModel(model);
        painelResultados.setViewportView(listaFuncionario);
        painelResultados.setVisible(true);

        listaFuncionario.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                Funcionario funcionario = listaFuncionario.getSelectedValue();
                campoTextoExibirID.setText(funcionario.getId().toString());
                campoTextoExibirNome.setText(funcionario.getNome());
                campoTextoExibirEmpresa.setText(funcionario.getEmpresa());
                campoTextoExibirProfissao.setText(funcionario.getProfissão());
                campoTextoExibirSalario.setText(funcionario.getSalario());
                PainelPrincipal.setSelectedComponent(abaExibirFuncionario);
            }
        });

    }//GEN-LAST:event_botaoBuscarActionPerformed

